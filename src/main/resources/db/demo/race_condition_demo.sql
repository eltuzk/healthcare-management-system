-- ==========================================================================================
-- DEMO 3: MÔ PHỎNG CÁC LỖI RACE CONDITION TRÊN BẢNG MEDICINE (KHO DƯỢC PHẨM)
-- Tệp tin này mô phỏng 3 lỗi tương tranh kho y tế cực kỳ phổ biến:
-- 1. Lost Update gây tồn kho âm.
-- 2. Write Skew giữa Kê đơn và Phát thuốc.
-- 3. Inconsistent Write do thiếu Rollback nguyên tử.
-- Ngôn ngữ: Oracle PL/SQL.
-- ==========================================================================================

-- ------------------------------------------------------------------------------------------
-- BƯỚC 1: THIẾT LẬP DỮ LIỆU THỬ NGHIỆM (SETUP)
-- ------------------------------------------------------------------------------------------
CREATE TABLE MEDICINE_DEMO (
    medicine_id VARCHAR2(50) PRIMARY KEY,
    medicine_name VARCHAR2(100),
    in_stock NUMBER(10)
);

CREATE TABLE PRESCRIPTION_DETAIL_DEMO (
    detail_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    prescription_id NUMBER,
    medicine_id VARCHAR2(50),
    quantity NUMBER(5)
);

